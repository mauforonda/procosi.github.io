Los medios de comunicación juegan un papel muy importante en el análisis, construcción y lectura social que se realiza de la violencia hacia las mujeres en la ciudad, así como en los estereotipos de género que siguen sustentado la misma. La opinión pública que los medios van creando ◊otherwords{favorable, hostil o indiferente} influye entre otros factores en el compromiso político con la problemática, la asignación de recursos para su solución, el apoyo para programas específicos de prevención. La apuesta por ciudades más democráticas y de mejor y mayor convivencia lleva implícita, como condición fundamental, la erradicación de la violencia contra las mujeres, su empoderamiento y la promoción de sus derechos como ciudadanas.

Las estrategias para una ciudad sin violencia contra las mujeres deben contemplar:

◊ul{
◊li{Una perspectiva acerca de la violencia hacia las mujeres y sus causas que la ubique como un problema sociocultural, que oriente posibles respuestas no restringidas al ámbito judicial y policial.}
◊li{La inclusión de los aspectos referidos a la asistencia a las victimas, a la prevención de la violencia y la promoción de los derechos de las mujeres, incluido el derecho a disfrutar de una ciudad segura y habitable.}
◊li{La responsabilidad de los distintos actores en el ámbito estatal, como son los ministerios, los gobiernos locales y sus dependencias, los órganos, el sistema de administración de justicia, las organizaciones de la sociedad civil y especialmente las organizaciones de mujeres que trabajan y promueven los derechos de las mismas.}
◊li{La necesidad de que las respuestas institucionales incluyan la perspectiva de género.}
◊li{Una temporalidad en la intencionalidad de su resolución ◊otherwords{a corto, a mediano, a largo plazo}, articuladas en una secuencia de acciones, a ser abordada a partir de la evaluación y monitoreo de los logros}
}

Se trata de pensar:

◊ul{
◊li{Estrategias para la sensibilización de la sociedad sobre el tema: campañas a través de distintos medios de comunicación, que permita desplegar acciones propositivas en el campo de los derechos de las mujeres, desarticulando mitos y creencias en torno a la violencia hacia las mujeres. Estas estrategias deben apuntar a generar conciencia respecto a la responsabilidad social y del Estado sobre la violencia hacia las mujeres para ser considerada su prevención como parte insoslayable de la agenda pública.}
◊li{Estrategias para el diseño urbano, mantenimiento y mejoramiento de la infraestructura vecinal y sus entornos, que contemplen alguno de los principios desarrollados en distintos estudios: iluminación y señalización adecuada, mixtura de usos que garanticen el control social de la calle y los espacios en distintos horarios, recorridos del transporte público que respondan a las necesidades de movilidad de las mujeres, seguridad en los paraderos, plazas y parques.}
◊li{Para ello, es necesario involucrar en el diseño, evaluación y seguimiento de las propuestas urbanas a los habitantes y en particular a las mujeres como expertas.}
◊li{Estrategias para articular y potenciar los recursos existentes en la administración local y otras jurisdicciones estatales, así como las organizaciones de sociedad civil, especialmente las comprometidas con los derechos de las mujeres.}
◊li{Recuperando los recursos institucionales existentes que se encuentran trabajando en relación con esta problemática. Las comisarías, las comisarías de mujeres, los Centros de Emergencia Mujer, las áreas de las municipalidades que trabajan el tema de género ◊otherwords{Programa de Promoción de la Mujer e Igualdad de Oportunidades y las Gerencias de desarrollo social} y las instancias que trabajan seguridad ciudadana ◊otherwords{Gerencia de servicios a la ciudad o sub Gerencia de control ciudadano}; organizaciones de mujeres comprometidas con el tema ◊otherwords{Promotoras legales frente a la violencia hacia la mujer, comités de vigilancia ciudadana} entre otras.}
◊li{Promoviendo políticas y presupuestos locales para la prevención y atención de la inseguridad ciudadana.}
◊li{Promoviendo presupuesto público regional y local para hacer que las ciudades sean seguras para las mujeres.}
