◊h2[#:id "3.2"]{Seguridad ciudadana y violencia de género◊sidenote{Diana Miloslavich, Lidia Sihuacollo, ◊citetitle{Ciudades sin violencia hacia las mujeres, ciudades sin violencia para todos}, Centro de la Mujer Peruana Flora Tristán, 2009.}}

◊h3{Violencia urbana y violencia de género}

La ◊concept{violencia urbana} es multifacética, multidimensional y compleja. Su existencia es producto de relaciones sociales inequitativas en las ciudades, ubicadas en contextos y procesos históricos específi cos. Las ciudades se han vuelto más violentas, y la violencia se ha hecho más visible. A esta visibilidad ha contribuido la existencia de muchos más actores sociales pugnando por el acceso a la ciudad/espacio público, evidenciándose una pluralidad de causas, de experiencias y vivencias de la violencia. Su mayor visibilidad ha generado lentamente un marco institucional múltiple y un conjunto de instituciones privadas y públicas que actúan sobre ella: gobiernos nacionales, gobiernos locales, organizaciones de mujeres, organizaciones de derechos humanos, etc.

En la violencia urbana intervienen diferentes dimensiones y escalas, que permiten distintos niveles de análisis. En un continuum, podemos encontrar múltiples dimensiones, que se implican e influyen entre sí: entre el nivel macro y el nivel micro está un conjunto de escalas que comprenden regiones geográficas, naciones, ciudades, barrios, grupos, hogares, cada una de ellos con sus propias exclusiones y violencias simbólicas o concretas.

A nivel de lo macroestructural ◊otherwords{tiempos y espacios extensos marcados por la creciente globalización de ámbitos políticos, económicos y culturales}, se desarrollan sistemas económicos con reglas de acumulación y de comercio que profundizan la inequidad entre países y regiones y acentúan la concentración de riqueza y exclusión de las mayorías. Lo macrorregional, caracterizado por un desigual desarrollo democrático y la consiguiente debilidad de la institucionalidad democrática, ha tenido una larga historia de violencia política, con efectos significativos en los países y en la visibilidad de la violencia de género. Ello porque la violencia política tendió a ocultar la violencia contra las mujeres en el espacio privado ◊otherwords{y también en el espacio público}.◊sidenote{Como lo expresan las violaciones a las mujeres consideradas ◊em{botín de todos los bandos} y que han llevado a que la Corte Internacional los considere ◊em{crímenes de lesa humanidad}.}

A niveles nacionales, fenómenos como el creciente debilitamiento del Estado-nación y la creciente privatización de los servicios y las políticas de bienestar han debilitado también las instituciones responsables de llevar a cabo medidas para la prevención, atención y castigo de la violencia.

En las ciudades se da una particular concepción del desarrollo urbano, expresada en crecimiento sin planificación, alta densidad demográfica, precariedad o dificultad para acceder a servicios en amplios sectores de la población, en relación con vivienda, iluminación, transporte, que alarga las distancias entre el hogar y los lugares de trabajo remunerado. La creciente privatización y la desregulación de los servicios de la ciudad impactan crecientemente en las mujeres, al exigirles más tiempo para suplir la falta de responsabilidad del Estado frente a la niñez o la tercera edad., condicionando y restringiendo así sus usos del tiempo. Todo ello debilita las dinámicas de cohesión social y produce ciudades espacialmente fracturadas en zonas, clases, culturas diferentes, con repercusiones en una organización espacial de las desigualdades e inequidades que impide que las personas se vean o imaginen y piensen como pares.

En estas condiciones, la violencia directa (robos, asaltos, agresiones) tiene más impacto real en los hombres, pero ejerce un impacto subjetivo y duradero en el imaginario de las mujeres, lo que limita fuertemente su movilidad y autonomía.

Una dimensión de violencia no considerada activamente dentro de la violencia urbana es la violencia contra las mujeres al interior del espacio privado, aunque aparece como la más persistente y la más extendida. Y ella se expresa en todos los sectores sociales, sin distinción de clase, raza, etnia, edad, orientación sexual o tiempo histórico.

◊h3{La violencia hacia las mujeres en la calle}

Como hemos mencionado la violencia e inseguridad afecta al conjunto de la ciudadanía, pero es vivida por hombres y mujeres de maneras distintas. La ◊concept{violencia contra las mujeres en las ciudades}, no solo es referida a los delitos tradicionales que dificultan la vida cotidiana, tales como los hurtos, asaltos, secuestros, violaciones y homicidios.

Inciden de igual manera, pero en diferente grados y modalidades, las manifestaciones de violencia efectiva o simbólica hacia las mujeres.

El derecho a la ciudad por parte de las mujeres se ve obstaculizado por la sensación de inseguridad y el temor a ser agredidas, lo que impacta directamente en su vida cotidiana, limitando su movilidad y autonomía para el uso y apropiación de la ciudad, particularmente en determinados horarios y lugares considerados o vivenciados peligrosos.

La peligrosidad de estos lugares se vincula a:

◊ul{
◊li{Las características físicas de los mismos (terrenos sin construir, iluminación insuficiente, zonas deterioradas, abandonadas y sin control social)}
◊li{La dinámica social que transcurre y se expresa en estos espacios, tales como la apropiación por parte de grupos de varones con conductas agresivas ◊otherwords{pandillaje, barras bravas, drogadicción, etc.}}
}

La percepción de inseguridad y los lugares asociados a la misma se vincula también a la existencia o no de redes sociales de protección entre vecin◊gender{os/as} y/o distintos grupos sociales y su mayor o menor identificación con el lugar en el que viven.

Específicamente las mujeres reconocen como lugares peligrosos:

◊ul{
◊li{Algunos parques}
◊li{Mercados y centros comerciales}
◊li{Paraderos de microbuses, táxis y moto-taxis}
◊li{Lugares descampados}
◊li{El centro de la ciudad}
◊li{Cruces de calle y en semáforos cuando se transita en vehículos}
◊li{Zonas aledañas a campos deportivos}
◊li{Zonas aledañas a discotecas, bares.}
}

Al abordar la violencia urbana desde las voces de las mujeres, ésta se manifiesta de distintas formas, pero tienen en común el hecho de que los agresores son en la mayoría de los casos varones, desdibujando así las fronteras de la violencia vivida en el espacio privado ◊otherwords{ejercida por individuos pertenecientes al círculo familiar cercano} y la violencia en el espacio público ◊otherwords{ejercida por desconocidos}.

Los relatos de las mujeres hacen referencia a:

◊ul{
◊li{Los delitos considerados comúnmente como tales (robos, asesinatos, arrebatos) que adquieren ciertas especificidades cuando la destinataria es una mujer: mayor violencia física o verbal, posibilidad de abuso sexual o violación.}
◊li{Las conductas agresivas de los varones en el espacio público: insinuaciones sexuales, ser miradas como objeto sexual, chistes y burlas, conductas invasivas de su espacio corporal, por ejemplo en el transporte público y en la calle luego de espectáculos deportivos.}
}

No podemos desconocer la doble victimización por la que atraviesan las mujeres, luego de un hecho de violencia. Esta se refiere a la nueva violencia que sufren las mujeres cuando intentan dar cuenta o denunciar la situación por la que han atravesado. Esta violencia es efectuada en gran medida por el propio Estado, a través de las instituciones policiales y judiciales a las que recurre la mujer para pedir justicia y contención.

También se ve sometida a este proceso cuando en los ámbitos familiares y comunitarios es responsabilizada de la violencia que sufrió ◊otherwords{◊citation{ella lo provocó}, ◊citation{porque no tuvo cuidado}}, o se subestima la agresión ◊otherwords{◊citation{no fue tan grave}}.

◊h3{Las consecuencias de la violencia}

La violencia hacia las mujeres no acaba en el mismo hecho violento, sino que sigue actuando a través de sus consecuencias, ya que las mujeres desarrollan sentimientos que atentan contra su autoestima y seguridad, al mismo tiempo que modifican su vida cotidiana, restringiendo sus movimientos en la ciudad y los espacios públicos, la participación política, cultural y en algunos casos hasta abandonan el trabajo o estudio.

◊ul{
◊li{Restricción del ejercicio de su ciudadanía.}
◊li{Miedo de circular libremente a cualquier hora, restricción de la movilidad.}
◊li{Dependencia de la protección ◊otherwords{de un hombre o de aparatos}}
◊li{Falta de confianza en si falta de autonomía}
◊li{Limitaciones de realizar actividades de tarde o noche.}
◊li{Aislamiento.}
◊li{Efectos sobre la salud física, psicológica y sexual.}
◊li{Obstáculos a la participación en la vida social ◊otherwords{esparcimiento, estudios, trabajo, activismo social o político}}
}

Cualquier situación de violencia sufrida por la mujer, impacta en el colectivo de mujeres como situación a la que están expuestas por pertenecer al mismo grupo. Si estas situaciones no son abordadas correctamente y no se disponen recursos sociales e institucionales para ello, se multiplicarán las acciones defensivas y de estrategias individuales que implican mayor restricción de movilidad para las mujeres en la ciudad.

La violencia no solo lesiona el derecho que tienen a la integridad física, psicológica y sexual, sino que les resta posibilidades de desarrollo y participación plena en la vida política, económica, social y cultural.

