◊h3{Qué estrategias podemos construir entre todos y todas}

A partir de lo planteado anteriormente, se hace necesario incorporar la perspectiva y la experiencia de las mujeres en la planificación de políticas publicas de seguridad en la ciudad.

Proponemos contrastar y diferenciar la mirada sobre las mujeres: de objetos ◊otherwords{dependientes de} a ciudadanas ◊otherwords{autónomas}. Esto permitirá avanzar en lineamientos de políticas que favorezcan el empoderamiento de las mujeres y su plena participación en le construcción de ciudades democráticas y equitativas.

Las mujeres deben ser el centro de la intervención:

