◊h3{Las mujeres tienen derecho a vivir una vida sin violencia}

◊ul{
◊li{Garantizar ciudades seguras para todos y en especial para las mujeres ◊otherwords{niñas, jóvenes, adultas y ancianas} es un derecho humano.}
◊li{Los derechos de las mujeres están enunciados en diversas declaraciones y convenciones internacionales, las cuales fueron ratificadas por nuestro país}
◊li{El ◊concept{derecho de las mujeres a vivir una vida sin violencia} se afirma en distintos Acuerdos Internacionales:}
}

◊blockquote["Declaración de la II Conferencia Mundial sobre Derechos Humanos (Viena, 1993)"]{Los derechos humanos de la mujer y de la niña son parte inalienable, integrante e indivisible de los derechos humanos universales ◊noise La violencia y todas las formas de acosos y explotación sexuales, en particular las derivadas de prejuicios culturales y de la trata internacional de personas son incompatibles con la dignidad y la valía de la persona humana y deben ser eliminadas.}

